public class Special {
    int special_img;
    String special_name;
    String special_type;

    public int getSpecial_img() {
        return special_img;
    }

    public void setSpecial_img(int special_img) {
        this.special_img = special_img;
    }

    public String getSpecial_name() {
        return special_name;
    }

    public void setSpecial_name(String special_name) {
        this.special_name = special_name;
    }

    public String getSpecial_type() {
        return special_type;
    }

    public void setSpecial_type(String special_type) {
        this.special_type = special_type;
    }
}
